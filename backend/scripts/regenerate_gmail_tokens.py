#!/usr/bin/env python3
"""
Script to regenerate Gmail OAuth tokens for the expense tracker.

This script helps you regenerate expired or revoked Gmail tokens by:
1. Generating a new authorization URL
2. Helping you exchange the authorization code for new tokens
3. Updating your environment configuration
"""

import sys
import os
from pathlib import Path

# Add backend directory to Python path
backend_path = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(backend_path))

from src.services.email_ingestion.auth import EmailAuthHandler
from src.utils.logger import get_logger
from src.utils.settings import get_settings

logger = get_logger(__name__)


def check_current_tokens():
    """Check the current token status"""
    print("üîç CHECKING CURRENT TOKEN STATUS")
    print("=" * 50)
    
    settings = get_settings()
    
    # Check if we have the basic configuration
    has_client_config = bool(settings.GOOGLE_CLIENT_ID and settings.GOOGLE_CLIENT_SECRET)
    has_refresh_token = bool(settings.GOOGLE_REFRESH_TOKEN)
    
    print(f"Client Configuration: {'‚úÖ Found' if has_client_config else '‚ùå Missing'}")
    print(f"Refresh Token: {'‚úÖ Found' if has_refresh_token else '‚ùå Missing'}")
    
    if has_client_config and has_refresh_token:
        print("\nüîÑ Testing token validity...")
        try:
            handler = EmailAuthHandler()
            # For validation, we only need the refresh token since access tokens expire quickly
            is_valid = handler.validate_credentials(
                None,  # No access token needed for validation
                settings.GOOGLE_REFRESH_TOKEN
            )
            print(f"Token Status: {'‚úÖ Valid' if is_valid else '‚ùå Expired/Invalid'}")
            return is_valid
        except Exception as e:
            print(f"Token Status: ‚ùå Error - {e}")
            return False
    else:
        print("‚ùå Cannot test tokens - missing configuration")
        return False


def generate_authorization_url():
    """Generate a new authorization URL"""
    print("\nüîó GENERATING AUTHORIZATION URL")
    print("=" * 50)
    
    try:
        handler = EmailAuthHandler()
        auth_url = handler.get_authorization_url()
        
        print("‚úÖ Authorization URL generated successfully!")
        print("\nüìã NEXT STEPS:")
        print("1. Copy the URL below and open it in your browser")
        print("2. Sign in with your Gmail account")
        print("3. Grant permissions to the application")
        print("4. Copy the authorization code from the callback URL")
        print("\nüîó AUTHORIZATION URL:")
        print("-" * 80)
        print(auth_url)
        print("-" * 80)
        
        return auth_url
    except Exception as e:
        print(f"‚ùå Error generating authorization URL: {e}")
        return None


def exchange_code_for_tokens(authorization_code: str):
    """Exchange authorization code for new tokens"""
    print("\nüîÑ EXCHANGING CODE FOR TOKENS")
    print("=" * 50)
    
    try:
        handler = EmailAuthHandler()
        token_info = handler.exchange_code_for_tokens(authorization_code)
        
        print("‚úÖ Successfully exchanged authorization code for tokens!")
        print("\nüìã NEW TOKEN INFORMATION:")
        print(f"Access Token: {token_info.get('access_token', 'N/A')[:20]}...")
        print(f"Refresh Token: {token_info.get('refresh_token', 'N/A')[:20]}...")
        print(f"Client ID: {token_info.get('client_id', 'N/A')}")
        print(f"Scopes: {', '.join(token_info.get('scopes', []))}")
        
        # Save tokens to environment file
        print("\nüíæ SAVING TOKENS TO ENVIRONMENT FILE...")
        handler.save_tokens_to_env(token_info)
        print("‚úÖ Tokens saved successfully!")
        
        return token_info
    except Exception as e:
        print(f"‚ùå Error exchanging code for tokens: {e}")
        return None


def test_new_tokens():
    """Test the newly generated tokens"""
    print("\nüß™ TESTING NEW TOKENS")
    print("=" * 50)
    
    try:
        settings = get_settings()
        handler = EmailAuthHandler()
        
        is_valid = handler.validate_credentials(
            None,  # No access token needed for validation
            settings.GOOGLE_REFRESH_TOKEN
        )
        
        if is_valid:
            print("‚úÖ New tokens are working correctly!")
            return True
        else:
            print("‚ùå New tokens are still not working")
            return False
    except Exception as e:
        print(f"‚ùå Error testing tokens: {e}")
        return False


def main():
    """Main function"""
    print("üîê GMAIL TOKEN REGENERATION TOOL")
    print("=" * 60)
    print()
    
    # Check current token status
    tokens_valid = check_current_tokens()
    
    if tokens_valid:
        print("\n‚úÖ Your current tokens are working fine!")
        print("No regeneration needed.")
        return
    
    print("\nüîÑ Tokens need to be regenerated. Let's fix this!")
    
    # Generate authorization URL
    auth_url = generate_authorization_url()
    if not auth_url:
        print("\n‚ùå Failed to generate authorization URL. Please check your configuration.")
        return
    
    # Get authorization code from user
    print("\n‚è≥ WAITING FOR AUTHORIZATION CODE...")
    print("After completing the OAuth flow, paste the authorization code below:")
    authorization_code = input("\nAuthorization Code: ").strip()
    
    if not authorization_code:
        print("‚ùå No authorization code provided. Exiting.")
        return
    
    # Exchange code for tokens
    token_info = exchange_code_for_tokens(authorization_code)
    if not token_info:
        print("\n‚ùå Failed to exchange code for tokens.")
        return
    
    # Test new tokens
    if test_new_tokens():
        print("\nüéâ SUCCESS! Your Gmail tokens have been regenerated successfully!")
        print("\nüìã SUMMARY:")
        print("- New tokens generated and saved")
        print("- Environment configuration updated")
        print("- Tokens validated and working")
        print("\n‚úÖ You can now run your expense tracker workflow again!")
    else:
        print("\n‚ùå Something went wrong. Please try the process again.")


if __name__ == "__main__":
    main()
